public class tasksheet113 {

    

    public static void main(String[] args) {
        
                
        int x = 1 , y = 2 ;  // result is now 3
        int z = x + y ;
        System.out.println(z);

        z -=  1; // result is now 2
        System.out.println(z);

        z *=  2; // result is now 4
        System.out.println(z);

        z /=  2; // result is now 2
        System.out.println(z);

        z += 8; // result is now 10
        System.out.println(z);
        z %= 7; // result is now 3
        System.out.println(z);

    }
}
